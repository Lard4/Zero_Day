public class Player {
    
    private String location;
    private String mood;
    
    public void setLocation(String location) {
        this.location = location;
    }
    
    public void setMood(String mood) {
        this.mood = mood;
    }
    
    public String getMood() {
        return mood;
    }
    
    public String getLocation() {
        return location;
    }
}